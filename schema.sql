-- ── 1. PROFILES (extends Supabase Auth users) ──────────────
create table if not exists profiles (
  id          uuid references auth.users on delete cascade primary key,
  name        text,
  email       text,
  phone       text,
  address     text,
  shirt_size  text,
  role        text default 'ambassador',  -- 'ambassador' or 'manager'
  created_at  timestamptz default now()
);

alter table profiles enable row level security;

drop policy if exists "profiles_select"        on profiles;
drop policy if exists "profiles_insert"        on profiles;
drop policy if exists "profiles_update_own"    on profiles;
drop policy if exists "profiles_update_manager"on profiles;

create policy "profiles_select"
  on profiles for select to authenticated using (true);

create policy "profiles_insert"
  on profiles for insert to authenticated
  with check (auth.uid() = id);

create policy "profiles_update_own"
  on profiles for update to authenticated
  using (auth.uid() = id);

create policy "profiles_update_manager"
  on profiles for update to authenticated
  using ((select role from profiles where id = auth.uid()) = 'manager');


-- ── 2. AUTO-CREATE PROFILE ON SIGNUP ──────────────────────
create or replace function handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into profiles (id, email, name, role)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'name', ''),
    coalesce(new.raw_user_meta_data->>'role', 'ambassador')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure handle_new_user();


-- ── 3. ROLE HELPER FUNCTION ────────────────────────────────
create or replace function get_my_role()
returns text language sql security definer stable as $$
  select coalesce(
    (select role from profiles where id = auth.uid()),
    'ambassador'
  );
$$;


-- ── 4. EVENTS ──────────────────────────────────────────────
create table if not exists events (
  id           bigint generated by default as identity primary key,
  name         text        not null,
  store        text,
  retailer     text,
  date         date,
  time         text,
  status       text        default 'upcoming',
  ambassador   text,
  product      text,
  budget       numeric     default 0,
  notes        text,
  units_sold   integer,
  samples      integer,
  sales_lift   numeric,
  created_at   timestamptz default now()
);

-- New columns for auth integration
alter table events add column if not exists ambassador_id uuid references profiles(id);
alter table events add column if not exists accepted      boolean default false;

alter table events enable row level security;

drop policy if exists "Public access"       on events;
drop policy if exists "events_manager"      on events;
drop policy if exists "events_amb_select"   on events;
drop policy if exists "events_amb_insert"   on events;
drop policy if exists "events_amb_update"   on events;

-- Managers: full access
create policy "events_manager"
  on events for all to authenticated
  using    (get_my_role() = 'manager')
  with check (get_my_role() = 'manager');

-- Ambassadors: read only their own events
create policy "events_amb_select"
  on events for select to authenticated
  using (
    get_my_role() = 'ambassador'
    and (
      ambassador_id = auth.uid()
      or ambassador = (select name from profiles where id = auth.uid())
    )
  );

-- Ambassadors: submit new event requests (status must be pending_approval)
create policy "events_amb_insert"
  on events for insert to authenticated
  with check (
    get_my_role() = 'ambassador'
    and status = 'pending_approval'
  );

-- Ambassadors: can accept (update accepted field) on their own events
create policy "events_amb_update"
  on events for update to authenticated
  using (
    get_my_role() = 'ambassador'
    and (
      ambassador_id = auth.uid()
      or ambassador = (select name from profiles where id = auth.uid())
    )
  );


-- ── 5. AMBASSADORS (roster / stats table) ─────────────────
create table if not exists ambassadors (
  id            bigint generated by default as identity primary key,
  name          text    not null,
  email         text,
  phone         text,
  status        text    default 'active',
  events_month  integer default 0,
  total_events  integer default 0,
  rating        numeric default 5.0,
  specialty     text,
  rate          numeric default 0,
  city          text,
  created_at    timestamptz default now()
);

alter table ambassadors enable row level security;

drop policy if exists "Public access"   on ambassadors;
drop policy if exists "amb_manager"     on ambassadors;
drop policy if exists "amb_select_all"  on ambassadors;

create policy "amb_manager"
  on ambassadors for all to authenticated
  using    (get_my_role() = 'manager')
  with check (get_my_role() = 'manager');

create policy "amb_select_all"
  on ambassadors for select to authenticated using (true);


