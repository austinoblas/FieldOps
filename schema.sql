-- ── 1. EVENTS ─────────────────────────────────────────────
create table if not exists events (
  id           bigint generated by default as identity primary key,
  name         text        not null,
  store        text,
  retailer     text,
  date         date,
  time         text,
  status       text        default 'upcoming',
  ambassador   text,
  product      text,
  budget       numeric     default 0,
  notes        text,
  units_sold   integer,
  samples      integer,
  sales_lift   numeric,
  created_at   timestamptz default now()
);

alter table events enable row level security;
create policy "Public access" on events for all using (true) with check (true);


-- ── 2. AMBASSADORS ────────────────────────────────────────
create table if not exists ambassadors (
  id            bigint generated by default as identity primary key,
  name          text    not null,
  email         text,
  phone         text,
  status        text    default 'active',
  events_month  integer default 0,
  total_events  integer default 0,
  rating        numeric default 5.0,
  specialty     text,
  rate          numeric default 0,
  city          text,
  created_at    timestamptz default now()
);

alter table ambassadors enable row level security;
create policy "Public access" on ambassadors for all using (true) with check (true);


-- ── 3. REPORTS ────────────────────────────────────────────
create table if not exists reports (
  id          bigint generated by default as identity primary key,
  event_name  text,
  ambassador  text,
  date        date,
  status      text    default 'pending',
  units_sold  integer default 0,
  samples     integer default 0,
  feedback    text,
  photos      integer default 0,
  sales_lift  numeric default 0,
  created_at  timestamptz default now()
);

alter table reports enable row level security;
create policy "Public access" on reports for all using (true) with check (true);


-- ── 4. PAYMENTS ───────────────────────────────────────────
create table if not exists payments (
  id          bigint generated by default as identity primary key,
  ambassador  text,
  event_name  text,
  date        date,
  hours       numeric default 0,
  rate        numeric default 0,
  expenses    numeric default 0,
  total       numeric default 0,
  status      text    default 'pending',
  created_at  timestamptz default now()
);

alter table payments enable row level security;
create policy "Public access" on payments for all using (true) with check (true);


-- ── 5. STORES ─────────────────────────────────────────────
create table if not exists stores (
  id           bigint generated by default as identity primary key,
  name         text    not null,
  retailer     text,
  address      text,
  manager      text,
  phone        text,
  priority     text    default 'medium',
  last_demo    text,
  total_demos  integer default 0,
  created_at   timestamptz default now()
);

alter table stores enable row level security;
create policy "Public access" on stores for all using (true) with check (true);


-- ── 6. SURVEYS ────────────────────────────────────────────
create table if not exists surveys (
  id            bigint generated by default as identity primary key,
  event_name    text,
  responses     integer default 0,
  rating        numeric default 0,
  top_feedback  text,
  date          date,
  created_at    timestamptz default now()
);

alter table surveys enable row level security;
create policy "Public access" on surveys for all using (true) with check (true);


