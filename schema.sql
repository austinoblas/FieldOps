alter table reports enable row level security;

drop policy if exists "Public access"      on reports;
drop policy if exists "rep_manager"        on reports;
drop policy if exists "rep_amb_select"     on reports;
drop policy if exists "rep_amb_insert"     on reports;
drop policy if exists "rep_amb_update"     on reports;

create policy "rep_manager"
  on reports for all to authenticated
  using    (get_my_role() = 'manager')
  with check (get_my_role() = 'manager');

create policy "rep_amb_select"
  on reports for select to authenticated
  using (
    get_my_role() = 'ambassador'
    and ambassador = (select name from profiles where id = auth.uid())
  );

create policy "rep_amb_insert"
  on reports for insert to authenticated
  with check (
    get_my_role() = 'ambassador'
    and ambassador = (select name from profiles where id = auth.uid())
  );

create policy "rep_amb_update"
  on reports for update to authenticated
  using (
    get_my_role() = 'ambassador'
    and ambassador = (select name from profiles where id = auth.uid())
    and status = 'pending'
  );


